package com.mtha.findmyfriends.utils;

public class Size {
    private int height = 0;
    private int width = 0;
    Size(int width, int height){
        this.width = width;
        this.height = height;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }
}
